periodo = str(input("Qual horário do dia prefere manha, tarde ou noite? Sua opção:  "))
print("---------------------------------------------------")
if(periodo == "manha"):
    print("Prefere o período da manhã.")
elif(periodo == "tarde"):
    print("Prefere o período da tarde.")
elif(periodo == "noite"):
    print("Prefere o período da noite.")
else:
    print("Período inválido!.")
